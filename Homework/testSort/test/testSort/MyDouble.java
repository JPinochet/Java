public class MyDouble
{
	//attributes
	private double 		value;

	/**
	 * 
	 */
	public MyDouble()
	{
	}

	/**
	 * @param value
	 */
	public MyDouble(double value)
	{
		this.value = value;
	}

	/**
	 * @return the value
	 */
	public double getValue()
	{
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(double value)
	{
		this.value = value;
	}

	public String toString()
	{
		return value+" ";
	}


}
